func TestGenerateDockerComposeNoDB(t *testing.T) {
	opts := ComposeOptions{
		BenchPath:    "/bench",
		AppName:      "nodb",
		Mode:         "dev",
		WebPort:      8000,
		SocketPort:   9000,
		IncludeDB:    false,
		IncludeRedis: true,
	}

	result := GenerateDockerCompose(opts)

	// Should not have db: service
	lines := strings.Split(result, "\n")
	for i, line := range lines {
		if strings.TrimSpace(line) == "db:" {
			// Check if this is a service definition (indented at service level)
			if i > 0 && strings.HasPrefix(lines[i], "  db:") {
				t.Error("should not include db service")
			}
		}
	}
}

