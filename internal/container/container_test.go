func TestWriteDockerCompose(t *testing.T) {
	tmpDir := t.TempDir()

	content := "version: '3.8'\nservices:\n  web:\n    image: test\n"
	err := WriteDockerCompose(tmpDir, content)
	if err != nil {
		t.Fatalf("failed to write docker-compose: %v", err)
	}

	// Read it back
	data, err := os.ReadFile(filepath.Join(tmpDir, "docker-compose.yml"))
	if err != nil {
		t.Fatalf("failed to read docker-compose: %v", err)
	}

	if string(data) != content {
		t.Errorf("content mismatch: got %s", string(data))
	}
}

