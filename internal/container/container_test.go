func TestDefaultComposeOptions(t *testing.T) {
	opts := DefaultComposeOptions("/path/to/bench")

	if opts.BenchPath != "/path/to/bench" {
		t.Errorf("expected BenchPath '/path/to/bench', got %s", opts.BenchPath)
	}
	if opts.Mode != "dev" {
		t.Errorf("expected Mode 'dev', got %s", opts.Mode)
	}
	if opts.WebPort != 8000 {
		t.Errorf("expected WebPort 8000, got %d", opts.WebPort)
	}
	if opts.SocketPort != 9000 {
		t.Errorf("expected SocketPort 9000, got %d", opts.SocketPort)
	}
	if opts.DBPort != 3306 {
		t.Errorf("expected DBPort 3306, got %d", opts.DBPort)
	}
	if opts.Database != "mariadb" {
		t.Errorf("expected Database 'mariadb', got %s", opts.Database)
	}
	if !opts.IncludeDB {
		t.Error("expected IncludeDB to be true")
	}
	if !opts.IncludeRedis {
		t.Error("expected IncludeRedis to be true")
	}
	if !opts.Volumes {
		t.Error("expected Volumes to be true")
	}
}

