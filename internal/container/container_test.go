func TestGenerateDockerComposeDevMode(t *testing.T) {
	opts := ComposeOptions{
		BenchPath:    "/bench",
		AppName:      "testapp",
		Mode:         "dev",
		WebPort:      8000,
		SocketPort:   9000,
		DBPort:       3306,
		Database:     "mariadb",
		IncludeDB:    true,
		IncludeRedis: true,
		Volumes:      true,
	}

	result := GenerateDockerCompose(opts)

	// Check header
	if !strings.Contains(result, "Generated by weg") {
		t.Error("expected header comment")
	}
	if !strings.Contains(result, "Mode: dev") {
		t.Error("expected dev mode in header")
	}

	// Check services
	if !strings.Contains(result, "web:") {
		t.Error("expected web service")
	}
	if !strings.Contains(result, "worker:") {
		t.Error("expected worker service")
	}
	if !strings.Contains(result, "scheduler:") {
		t.Error("expected scheduler service")
	}
	if !strings.Contains(result, "socketio:") {
		t.Error("expected socketio service")
	}
	if !strings.Contains(result, "db:") {
		t.Error("expected db service")
	}
	if !strings.Contains(result, "redis-cache:") {
		t.Error("expected redis-cache service")
	}
	if !strings.Contains(result, "redis-queue:") {
		t.Error("expected redis-queue service")
	}

	// Check dev mode volumes
	if !strings.Contains(result, "./apps:/home/frappe/frappe-bench/apps") {
		t.Error("expected dev mode app volume mount")
	}

	// Check MariaDB
	if !strings.Contains(result, "mariadb:10.6") {
		t.Error("expected mariadb image")
	}
}

