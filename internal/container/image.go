package container

import (
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"strings"
)

// ImageOptions configures container image building
type ImageOptions struct {
	BenchPath  string
	AppName    string   // Primary app name for tagging
	Apps       []string // Apps to include
	Tag        string   // Image tag
	Registry   string   // Registry to push to
	Target     string   // Build target: web, worker, all
	Platform   string   // Target platform(s): linux/amd64,linux/arm64
	FrappeBase string   // Base image from frappe_docker
	Push       bool     // Push after building
	NoCache    bool     // Build without cache
	BuildArgs  map[string]string
	Verbose    bool
}

// DefaultImageOptions returns sensible defaults
func DefaultImageOptions(benchPath string) ImageOptions {
	return ImageOptions{
		BenchPath:  benchPath,
		Target:     "all",
		Platform:   "linux/amd64",
		FrappeBase: "frappe/bench:latest",
		BuildArgs:  make(map[string]string),
	}
}

// GenerateDockerfile creates a multi-stage Dockerfile for the bench
func GenerateDockerfile(opts ImageOptions) string {
	// Determine apps to copy
	appsArg := strings.Join(opts.Apps, " ")

	dockerfile := fmt.Sprintf(`# Generated by weg - Multi-stage Dockerfile for Frappe apps
# Base: %s

ARG FRAPPE_VERSION=15
ARG PYTHON_VERSION=3.11

# =============================================================================
# Stage 1: Builder - Install apps and build assets
# =============================================================================
FROM %s as builder

ARG FRAPPE_VERSION
ARG PYTHON_VERSION

# Set working directory
WORKDIR /home/frappe/frappe-bench

# Copy apps
COPY apps/ apps/

# Install app dependencies
RUN for app in %s; do \
    if [ -d "apps/$app" ]; then \
        echo "Installing $app..." && \
        ./env/bin/pip install -e apps/$app --no-cache-dir; \
    fi; \
done

# Build frontend assets
RUN bench build --production

# =============================================================================
# Stage 2: Production - Minimal runtime image
# =============================================================================
FROM %s as production

ARG FRAPPE_VERSION
ARG PYTHON_VERSION

WORKDIR /home/frappe/frappe-bench

# Copy installed apps from builder
COPY --from=builder /home/frappe/frappe-bench/apps apps/
COPY --from=builder /home/frappe/frappe-bench/sites/assets sites/assets/
COPY --from=builder /home/frappe/frappe-bench/env env/

# Copy sites configuration
COPY sites/ sites/

# Set environment
ENV FRAPPE_BENCH_ROOT=/home/frappe/frappe-bench

# Expose ports
EXPOSE 8000 9000

# Health check
HEALTHCHECK --interval=30s --timeout=10s --start-period=5s --retries=3 \
    CMD curl -f http://localhost:8000/api/method/ping || exit 1

# Default command
CMD ["bench", "serve", "--port", "8000"]

# =============================================================================
# Stage 3: Web - Gunicorn web server
# =============================================================================
FROM production as web

CMD ["gunicorn", "--bind", "0.0.0.0:8000", "--workers", "4", "--timeout", "120", "frappe.app:application"]

# =============================================================================
# Stage 4: Worker - Background job worker
# =============================================================================
FROM production as worker

CMD ["bench", "worker", "--queue", "default,short,long"]

# =============================================================================
# Stage 5: Scheduler - Job scheduler
# =============================================================================
FROM production as scheduler

CMD ["bench", "schedule"]

# =============================================================================
# Stage 6: Socketio - Real-time communication
# =============================================================================
FROM production as socketio

EXPOSE 9000

CMD ["node", "/home/frappe/frappe-bench/apps/frappe/socketio.js"]
`, opts.FrappeBase, opts.FrappeBase, appsArg, opts.FrappeBase)

	return dockerfile
}

