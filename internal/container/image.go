// BuildImage builds a container image
func BuildImage(opts ImageOptions) error {
	// Generate Dockerfile if it doesn't exist
	dockerfilePath := filepath.Join(opts.BenchPath, "Dockerfile")
	if _, err := os.Stat(dockerfilePath); os.IsNotExist(err) {
		dockerfile := GenerateDockerfile(opts)
		if err := os.WriteFile(dockerfilePath, []byte(dockerfile), 0644); err != nil {
			return fmt.Errorf("failed to write Dockerfile: %w", err)
		}
	}

	// Generate .dockerignore
	dockerignorePath := filepath.Join(opts.BenchPath, ".dockerignore")
	if _, err := os.Stat(dockerignorePath); os.IsNotExist(err) {
		if err := os.WriteFile(dockerignorePath, []byte(GenerateDockerignore()), 0644); err != nil {
			return fmt.Errorf("failed to write .dockerignore: %w", err)
		}
	}

	// Determine image tag
	tag := opts.Tag
	if tag == "" {
		tag = fmt.Sprintf("%s:latest", opts.AppName)
	}
	if opts.Registry != "" {
		tag = fmt.Sprintf("%s/%s", opts.Registry, tag)
	}

	// Build command
	cmdArgs := []string{"build"}

	// Target
	if opts.Target != "" && opts.Target != "all" {
		cmdArgs = append(cmdArgs, "--target", opts.Target)
	}

	// Platform
	if opts.Platform != "" {
		cmdArgs = append(cmdArgs, "--platform", opts.Platform)
	}

	// Tag
	cmdArgs = append(cmdArgs, "-t", tag)

	// Build args
	for k, v := range opts.BuildArgs {
		cmdArgs = append(cmdArgs, "--build-arg", fmt.Sprintf("%s=%s", k, v))
	}

	// No cache
	if opts.NoCache {
		cmdArgs = append(cmdArgs, "--no-cache")
	}

	// Context
	cmdArgs = append(cmdArgs, ".")

	// Try podman first, then docker
	builder := "docker"
	if _, err := exec.LookPath("podman"); err == nil {
		builder = "podman"
	}

	cmd := exec.Command(builder, cmdArgs...)
	cmd.Dir = opts.BenchPath
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr

	if opts.Verbose {
		fmt.Printf("Running: %s %s\n", builder, strings.Join(cmdArgs, " "))
	}

	if err := cmd.Run(); err != nil {
		return fmt.Errorf("build failed: %w", err)
	}

	// Push if requested
	if opts.Push {
		return PushImage(tag, opts.Verbose)
	}

	return nil
}

// PushImage pushes an image to a registry
func PushImage(tag string, verbose bool) error {
	builder := "docker"
	if _, err := exec.LookPath("podman"); err == nil {
		builder = "podman"
	}

	cmd := exec.Command(builder, "push", tag)
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr

	if verbose {
		fmt.Printf("Pushing: %s\n", tag)
	}

	return cmd.Run()
}

// ListImages lists local images matching the app
func ListImages(appName string) ([]string, error) {
	builder := "docker"
	if _, err := exec.LookPath("podman"); err == nil {
		builder = "podman"
	}

	cmd := exec.Command(builder, "images", "--format", "{{.Repository}}:{{.Tag}}", appName)
	output, err := cmd.Output()
	if err != nil {
		return nil, err
	}

	lines := strings.Split(strings.TrimSpace(string(output)), "\n")
	var images []string
	for _, line := range lines {
		if line != "" {
			images = append(images, line)
		}
	}
	return images, nil
}
